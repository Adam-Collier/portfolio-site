const lowers = new Set([...conjunctions, ...articles, ...prepositions]);

export const toTitleCase = (slug) =>
  slug
    .split('-')
    .map((word) => {
      if (lowers.has(word)) {
        return word;
      }
      return word.charAt(0).toUpperCase() + word.slice(1);
    })
    .join(' ');
